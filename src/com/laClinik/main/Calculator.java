package com.laClinik.main;

public class Calculator {
    private int i;
    private int x;

    public Calculator() {

    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int add(int i, int x) {
        return i + x;
    }
}


